    private void habilitarDesabilitarCampos(boolean condicao) {
        this.txtNamePro.setEnabled(condicao);
        this.txtValorPro.setEnabled(condicao);
        this.txtEstoquePro.setEnabled(condicao);
        this.btnSalvarPro.setEnabled(condicao);
        this.txtCodBarra.setEnabled(condicao);
    }

    private void limparCampos() {
        this.txtCodPro.setText("");
        this.txtNamePro.setText("");
        this.txtValorPro.setText("");
        this.txtEstoquePro.setText("");
        this.txtCodBarra.setText("");
    }

    private void carregarProdutos() {
        this.listaModelProdutos = this.produtoController.retornaListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) this.tbPro.getModel();
        modelo.setNumRows(0);

        int cont = listaModelProdutos.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                this.listaModelProdutos.get(i).getId(),
                this.listaModelProdutos.get(i).getName(),
                this.listaModelProdutos.get(i).getPrice(),
                this.listaModelProdutos.get(i).getEstoque(),
                this.listaModelProdutos.get(i).getCodBarra()
            });
        }
    }

