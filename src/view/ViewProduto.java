    private void btnSalvarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarProActionPerformed
        if (this.salvarAlterar.equals("salvar")) {
            this.salvarProduto();
        } else if (this.salvarAlterar.equals("editar")) {
            this.alterarProduto();
        }
    }//GEN-LAST:event_btnSalvarProActionPerformed

    private void btnExcluirProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirProActionPerformed
        // TODO add your handling code here:
        int linha = this.tbPro.getSelectedRow();
        try {
            int codProduto = (int) this.tbPro.getValueAt(linha, 0);
            int result = JOptionPane.showConfirmDialog(this, "Tem certeza que dejesa excluir?", "ATENÇÃO", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                if (this.produtoController.excluirProdutoController(codProduto)) {
                    JOptionPane.showMessageDialog(this, "Produto excluido com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarProdutos();
                    this.limparCampos();
                    this.habilitarDesabilitarCampos(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Falha ao excluir o produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            this.habilitarDesabilitarCampos(false);
        }

    }//GEN-LAST:event_btnExcluirProActionPerformed

