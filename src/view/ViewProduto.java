    private void btnEditarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProActionPerformed
        // TODO add your handling code here:
        this.salvarAlterar = "editar";
        this.habilitarDesabilitarCampos(true);
        int linha = this.tbPro.getSelectedRow();

        try {
            int codProduto = (int) this.tbPro.getValueAt(linha, 0);
            this.modelProduto = this.produtoController.retornaProdutoController(codProduto);
            this.txtCodPro.setText(String.valueOf(this.modelProduto.getId()));
            this.txtNamePro.setText(this.modelProduto.getName());
            this.txtValorPro.setText(String.valueOf(this.modelProduto.getPrice()).format("%.2f", this.modelProduto.getPrice()));
            this.txtEstoquePro.setText(String.valueOf(this.modelProduto.getEstoque()));
            this.txtCodBarra.setText(String.valueOf(this.modelProduto.getCodBarra()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            this.habilitarDesabilitarCampos(false);
        }
    }//GEN-LAST:event_btnEditarProActionPerformed

    private void salvarProduto() {
        try {
            this.modelProduto.setName(this.txtNamePro.getText().toUpperCase());
            this.modelProduto.setEstoque(Integer.parseInt(this.txtEstoquePro.getText()));
            this.modelProduto.setPrice(Double.parseDouble(String.valueOf(this.txtValorPro.getText()).replaceAll(",", "")));
            this.modelProduto.setCodBarra(this.txtCodBarra.getText());
            if (this.produtoController.salvarProdutoController(this.modelProduto) > 0) {
                JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                this.carregarProdutos();
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao cadastrar o produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Falha ao cadastrar o produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void alterarProduto() {
        this.modelProduto.setName(this.txtNamePro.getText());
        this.modelProduto.setEstoque(Integer.parseInt(this.txtEstoquePro.getText()));
        this.modelProduto.setPrice(this.formatador.converteVirgulaParaPonto(this.txtValorPro.getText()));
        this.modelProduto.setCodBarra(this.txtCodBarra.getText());
        if (this.produtoController.alterarProdutoController(this.modelProduto)) {
            JOptionPane.showMessageDialog(this, "Produto atualizado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Falha ao atualizar o produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
