    private void salvarVenda() {
        int cont, codProduto = 0, codVenda = 0;

        this.modelVenda = new ModelVenda();
        this.modelVenda.setIdClient(50);
        try {
            this.modelVenda.setDataVenda(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            Logger.getLogger(ViewPDV.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.modelVenda.setTotal(Double.parseDouble(this.txtSubTotalPDV.getText().replaceAll(",", ".")));
        this.modelVenda.setVenDesconto(Double.parseDouble(String.valueOf(this.viewPagamentoPDV.getDesconto()).replaceAll(",", ".")));
        this.modelVenda.setValor(Double.parseDouble(String.valueOf(this.viewPagamentoPDV.getValorTotal()).replaceAll(",", ".")));

        cont = this.tbPDV.getRowCount();
        codVenda = this.controllerVenda.salvarVendaController(this.modelVenda);
        for (int i = 0; i < cont; i++) {
            codProduto = (int) this.tbPDV.getValueAt(i, 1);
            this.modelVendaProduto = new ModelVendaProduto();
            this.modelProduto = new ModelProduto();
            this.modelVendaProduto.setIdProduto(codProduto);
            this.modelVendaProduto.setIdVenda(codVenda);
            this.modelVendaProduto.setVenProValor((double) this.tbPDV.getValueAt(i, 4));
            this.modelVendaProduto.setVenProQtd(Integer.parseInt(this.tbPDV.getValueAt(i, 3).toString()));

            this.modelProduto.setId(codProduto);
            this.modelProduto.setEstoque(this.controllerProduto.retornaProdutoController(codProduto).getEstoque() - Integer.parseInt(this.tbPDV.getValueAt(i, 3).toString()));

            this.listaModelVendaProduto.add(this.modelVendaProduto);
            this.listaModelProduto.add(this.modelProduto);
        }

        if (this.controllerVendaProduto.salvarVendaProdutoController(this.listaModelVendaProduto)) {
            this.controllerProduto.alterarEstoqueProdutoController(this.listaModelProduto);
            this.jTitleProdutoPDV.setText("OBRIGADO E VOLTE SEMPRE! :)");
            JOptionPane.showMessageDialog(this, "Venda realizada com sucesso!");
            this.jTitleProdutoPDV.setText("CAIXAR LIVRE");
            this.limparCampo();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar produtos na venda!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void inserirProduto(java.awt.event.KeyEvent evt) {
        DefaultTableModel modelo = (DefaultTableModel) this.tbPDV.getModel();
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            try {
                this.modelProduto = this.controllerProduto.getCodigoBarraController(String.valueOf(Long.parseLong(this.txtBarraCod.getText())));
                this.jTitleProdutoPDV.setText(this.modelProduto.getName());
                qtd = Integer.parseInt(JOptionPane.showInputDialog("Informe a quantidade.", 1));
                modelo.addRow(new Object[]{
                    modelo.getRowCount() + 1,
                    this.modelProduto.getId(),
                    this.modelProduto.getName(),
                    qtd,
                    this.modelProduto.getPrice(),
                    qtd * this.modelProduto.getPrice()
                });
                this.jValorUniPDV.setText(String.valueOf(this.modelProduto.getPrice()).format("%.2f", this.modelProduto.getPrice()));
                this.jQtdPDV.setText(String.valueOf(qtd));
                this.txtSubTotalPDV.setText(String.valueOf(this.somarValorTotal()).format("%.2f", this.somarValorTotal()));
                this.txtBarraCod.setText("");
            } catch (Exception ex) {
            }
        }
    }

    private float somarValorTotal() {
        float soma = 0, valor = 0;
        int cont = this.tbPDV.getRowCount();

        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(this.tbPDV.getValueAt(i, 5)));
            soma += valor;
        }
        return soma;
    }

    private void limparCampo() {
        this.jValorUniPDV.setText("");
        this.jQtdPDV.setText("");
        this.txtSubTotalPDV.setText("");
        this.jValorTotalPDV.setText("");
        DefaultTableModel modelo = (DefaultTableModel) this.tbPDV.getModel();
        modelo.setRowCount(0);
    }

