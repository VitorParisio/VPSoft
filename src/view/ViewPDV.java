    private void jExcluirPDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jExcluirPDVActionPerformed
        // TODO add your handling code here:
        int qtdLinha = this.tbPDV.getRowCount();
        if (qtdLinha < 1) {
            JOptionPane.showMessageDialog(this, "Não há item(s) para excluir.", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                DefaultTableModel modelo = (DefaultTableModel) this.tbPDV.getModel();
                int linha = Integer.parseInt(JOptionPane.showInputDialog("Número do item a excluir."));
                modelo.removeRow(linha - 1);
                this.txtSubTotalPDV.setText(String.valueOf(this.somarValorTotal()).format("%.2f", this.somarValorTotal()));
                this.jValorUniPDV.setText("0,00");
                this.jQtdPDV.setText("0");
                this.jTitleProdutoPDV.setText("CAIXA OCUPADO");
                for (int i = 0; i < qtdLinha; i++) {
                    modelo.setValueAt(i + 1, i, 0);
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jExcluirPDVActionPerformed

