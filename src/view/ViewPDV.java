    private void jMenuFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuFinalizarActionPerformed
        // TODO add your handling code here:
        try {
            this.viewPagamentoPDV = new ViewPagamentoPDV(this, true);
            this.viewPagamentoPDV.setValorTotal(Float.parseFloat(this.txtSubTotalPDV.getText().replaceAll(",", ".")));
            this.viewPagamentoPDV.setTextValorTotal();
            this.viewPagamentoPDV.setVisible(true);

            this.jValorTotalPDV.setText(String.valueOf(this.viewPagamentoPDV.getValorTotal()).format("%.2f", this.viewPagamentoPDV.getValorTotal()));
            if (this.viewPagamentoPDV.isFinalizar()) {
                this.salvarVenda();
            } else {
                JOptionPane.showMessageDialog(this, "Venda cancelada!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                this.jTitleProdutoPDV.setText("CAIXA OCUPADO");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Insira produto(s) para finalizar a venda", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jMenuFinalizarActionPerformed

