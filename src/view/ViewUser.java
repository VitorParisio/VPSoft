    private void btnSalvarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarUserActionPerformed
        // TODO add your handling code here:
        if (this.txtNameUser.getText().equals("") || this.txtLogin.getText().equals("") || this.txtSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Favor prenncher os campos obrigatórios!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                this.modelUsuario.setId(Integer.parseInt(this.txtCodUser.getText()));
            } catch (NumberFormatException e) {

            }
            this.modelUsuario.setName(this.txtNameUser.getText());
            this.modelUsuario.setLogin(this.txtLogin.getText());
            this.modelUsuario.setSenha(this.txtSenha.getText());
            if (this.salvarAlterar.equals("salvar")) {
                if (this.controllerUsuario.salvarUserController(this.modelUsuario) > 0) {
                    JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarUsuarios();
                    this.limparCampo();
                    this.habilitarDesabilitarCampo(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar o usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                if (this.controllerUsuario.atualizarUserController(this.modelUsuario)) {
                    JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarUsuarios();
                    this.limparCampo();
                    this.habilitarDesabilitarCampo(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao atualizar o usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarUserActionPerformed

    private void btnExcluirUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirUserActionPerformed
        // TODO add your handling code here:
        int linha = this.tbUser.getSelectedRow();

        try {
            int codUsuario = (int) this.tbUser.getValueAt(linha, 0);
            int result = JOptionPane.showConfirmDialog(this, "Tem certeza que dejesa excluir?", "ATENÇÃO", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                if (this.controllerUsuario.excluirUserController(codUsuario)) {
                    JOptionPane.showMessageDialog(this, "Usuário excluido com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarUsuarios();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir o usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            this.habilitarDesabilitarCampo(false);
        }

    }//GEN-LAST:event_btnExcluirUserActionPerformed

    private void btnEditarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarUserActionPerformed
        // TODO add your handling code here:
        int linha = this.tbUser.getSelectedRow();
        this.salvarAlterar = "alterar";
        try {
            int codUser = (int) this.tbUser.getValueAt(linha, 0);

            this.modelUsuario = this.controllerUsuario.getUserController(codUser);
            this.txtCodUser.setText(String.valueOf(this.modelUsuario.getId()));
            this.txtNameUser.setText(this.modelUsuario.getName());
            this.txtLogin.setText(this.modelUsuario.getLogin());
            this.txtSenha.setText(this.modelUsuario.getSenha());
            this.habilitarDesabilitarCampo(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            this.habilitarDesabilitarCampo(false);
        }
    }//GEN-LAST:event_btnEditarUserActionPerformed

