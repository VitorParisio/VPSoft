    private void carregarUsuarios() {
        this.listaModelUsuario = this.controllerUsuario.getListaUserController();
        DefaultTableModel modelo = (DefaultTableModel) this.tbUser.getModel();
        modelo.setNumRows(0);

        int cont = this.listaModelUsuario.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                this.listaModelUsuario.get(i).getId(),
                this.listaModelUsuario.get(i).getName(),
                this.listaModelUsuario.get(i).getLogin()
            });
        }
    }

    private void habilitarDesabilitarCampo(boolean condicao) {
        this.txtNameUser.setEnabled(condicao);
        this.txtLogin.setEnabled(condicao);
        this.txtSenha.setEnabled(condicao);
        this.btnSalvarUser.setEnabled(condicao);
        this.btnSalvarUser.setEnabled(condicao);
    }

    private void limparCampo() {
        this.txtCodUser.setText("");
        this.txtNameUser.setText("");
        this.txtLogin.setText("");
        this.txtSenha.setText("");
    }

