    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (this.txtClient.getText().equals("") || this.txtFone.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Favor prenncher os campos obrigatórios!");
        } else {
            try {
                this.modelCliente.setName(this.txtClient.getText().toUpperCase());
            } catch (NumberFormatException e) {
                e.printStackTrace();
            }
            this.modelCliente.setEndereco(this.txtAddr.getText());
            this.modelCliente.setBairro(this.txtBairro.getText());
            this.modelCliente.setCidade(this.txtCity.getText());
            this.modelCliente.setUf(this.ufComboBox.getSelectedItem().toString());
            this.modelCliente.setCep(this.txtCep.getText());
            this.modelCliente.setFone(this.txtFone.getText());
            if (this.salvarAlterar.equals("salvar")) {
                if (this.controllerCliente.salvarClienteController(this.modelCliente) > 0) {
                    JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarCliente();
                    this.habilidaDesabilitaCampos(false);
                    this.limparCampos();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar o cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                if (this.controllerCliente.atualizarClienteController(this.modelCliente)) {
                    JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarCliente();
                    this.habilidaDesabilitaCampos(false);
                    this.limparCampos();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao atualizar o cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirClienteActionPerformed
        // TODO add your handling code here:
        int linha = this.jtCliente.getSelectedRow();
        try {
            int codCliente = (int) this.jtCliente.getValueAt(linha, 0);
            int result = JOptionPane.showConfirmDialog(this, "Tem certeza que dejesa excluir?", "ATENÇÃO", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (result == JOptionPane.YES_OPTION) {
                if (this.controllerCliente.excluirClienteController(codCliente)) {
                    JOptionPane.showMessageDialog(this, "Cliente excluido com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    this.carregarCliente();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir o cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            this.habilidaDesabilitaCampos(false);
        }

    }//GEN-LAST:event_btnExcluirClienteActionPerformed

