    private void habilidaDesabilitaCampos(boolean condicao) {
        this.txtClient.setEnabled(condicao);
        this.txtAddr.setEnabled(condicao);
        this.txtBairro.setEnabled(condicao);
        this.txtCity.setEnabled(condicao);
        this.ufComboBox.setEnabled(condicao);
        this.txtCep.setEnabled(condicao);
        this.txtFone.setEnabled(condicao);
        this.btnSalvar.setEnabled(condicao);
    }

    private void limparCampos() {
        this.txtCod.setText("");
        this.txtClient.setText("");
        this.txtAddr.setText("");
        this.txtBairro.setText("");
        this.txtCity.setText("");
        this.txtCep.setText("");
        this.txtFone.setText("");
    }

    private void carregarCliente() {
        this.listaModelCliente = controllerCliente.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) this.jtCliente.getModel();
        modelo.setNumRows(0);
        int cont = this.listaModelCliente.size();

        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                this.listaModelCliente.get(i).getId(),
                this.listaModelCliente.get(i).getName(),
                this.listaModelCliente.get(i).getFone()
            });
        }
    }

