    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        this.salvarAlterar = "alterar";
        int linha = this.jtCliente.getSelectedRow();

        try {
            int codCliente = (int) this.jtCliente.getValueAt(linha, 0);
            this.modelCliente = this.controllerCliente.getClienteController(codCliente);
            this.txtCod.setText(String.valueOf(modelCliente.getId()));
            this.txtClient.setText(modelCliente.getName());
            this.txtAddr.setText(modelCliente.getEndereco());
            this.txtBairro.setText(modelCliente.getBairro());
            this.txtCity.setText(modelCliente.getCidade());
            this.ufComboBox.setSelectedItem(modelCliente.getUf());
            this.txtCep.setText(modelCliente.getCep());
            this.txtFone.setText(modelCliente.getFone());

            this.habilidaDesabilitaCampos(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            this.habilidaDesabilitaCampos(false);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSearchClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchClientActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.jtCliente.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.jtCliente.setRowSorter(classificador);
        String texto = this.txtSearchClient.getText().toUpperCase();

        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_btnSearchClientActionPerformed

