    private void login(){
        if (this.txtLogin.getText().equals("") || this.txtSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Favor preencher os campos para o acesso!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            this.modelUsuario.setLogin(this.txtLogin.getText());
            this.modelUsuario.setSenha(String.valueOf(this.txtSenha.getPassword()));
            if (this.controllerUsuario.getValidarUserController(this.modelUsuario)) {
               this.modelUsuario = this.controllerUsuario.getUserController(this.txtLogin.getText());
               this.modelSessionUser.id = this.modelUsuario.getId();
               this.modelSessionUser.name = this.modelUsuario.getName();
               this.modelSessionUser.login = this.modelUsuario.getLogin();
                new ViewMain().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "UsuÃ¡rio e/ou senha invÃ¡lido(s)!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
